package StringPractice;

public class StringCharAtMethod {
    public static void main(String[] args) {

        //charAt method: start index from 0
        //extract character of string

        String s = "Welcome";
        System.out.println(s.charAt(3));//c
        System.out.println(s.charAt(5));// m
        System.out.println(s.charAt(0));// w

    }
}
